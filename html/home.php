    <h2>Sweet and Simple API</h2>

    <hr>

    <h4>Simple Heads</h4>

    <p>Unlike the PayPal API, we keep things nice and simple. For basic usage just provide a username:</p>
    <pre>&#60;img src="https://minotar.net/avatar/<span class="blue">clone1018</span>"&#62;</pre>

    <p>You can also set a size. We use pixels and we only need the <span class="red">width</span>. Just add it to the
        end.</p>
    <pre>&#60;img src="https://minotar.net/avatar/<span class="blue">clone1018</span>/<span class="red">100</span>"&#62;</pre>

    <p>And because some services require an extension, we've added simple support for it. Just add <span class="green">.png</span>
        to the end.</p>
    <pre>&#60;img src="https://minotar.net/avatar/<span class="blue">clone1018</span>/<span class="red">100</span><span class="green">.png</span>"&#62;</pre>

    <br>

    <h4>Avatar With Helm</h4>

    <p>Sometimes you want to display a helm too, that's fine with this endpoint:</p>
    <pre>&#60;img src="https://minotar.net/helm/<span class="blue">clone1018</span>/<span class="red">100</span><span class="green">.png</span>"&#62;</pre>

    <br>

    <h4>User's Skin</h4>

    <p>You can even use Minotar's API to get a users skin. <em>We're adding more soon!</em></p>
    <pre>&#60;img src="https://minotar.net/skin/<span class="blue">clone1018</span>"&#62;</pre>

    <p>You can also set the browser to download the image by using:</p>
    <pre>https://minotar.net/download/<span class="blue">clone1018</span></pre>

    <br>

    <h4>Random Skin</h4>

    <p>Randomly generated head? We got that too.</p>
    <pre>&#60;img src="https://minotar.net/random/<span class="red">100</span><span class="green">.png</span>"&#62;</pre>

    <br>

    <h4>View all Minotars</h4>

    <p>You can browse through all the <a href="<?php echo URL; ?>all/skin">skins</a>,
        <a href="<?php echo URL; ?>all/head">heads</a> and <a href="<?php echo URL; ?>all/helm">helms</a>.</p>

    <br>

    <h4>Refresh your Minotar</h4>

    <p>If you have changed your skin you can force Minotar to update your avatar by visiting the "refresh" link in your
        browser:</p>
    <pre>https://minotar.net/refresh/<span class="blue">clone1018</span></pre>

    <br>

